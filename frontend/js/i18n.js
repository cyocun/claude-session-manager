export function detectLang() {
    return localStorage.getItem('csm-lang') || (navigator.language.startsWith('ja') ? 'ja' : 'en');
}
export function translate(lang, key) {
    const dict = I18N[lang] || I18N.en;
    return dict[key] || I18N.en[key] || key;
}
