type I18nDict = Record<string, string>;

export function detectLang(): string {
  return localStorage.getItem('csm-lang') || (navigator.language.startsWith('ja') ? 'ja' : 'en');
}

export function translate(lang: string, key: string): string {
  const dict = (I18N as Record<string, I18nDict>)[lang] || (I18N.en as unknown as I18nDict);
  return dict[key] || (I18N.en as unknown as I18nDict)[key] || key;
}
